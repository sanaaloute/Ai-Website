#!/usr/bin/env node
/* eslint-disable @typescript-eslint/no-require-imports -- CommonJS Node build script */
/**
 * Bundles the visual-editing client scripts that run inside the sandbox preview
 * iframe into a single file that the backend injects into every template.
 *
 * Source: frontend/select-edit-feature/*.js
 * Output:  backend/src/templates/_shared/ai-website-editor-bridge.js
 *
 * Run from the frontend workspace root:
 *   npm run build:editor-bridge
 */

const fs = require('fs');
const path = require('path');

const SOURCE_DIR = path.resolve(__dirname, '..', 'select-edit-feature');
const OUTPUT_FILE = path.resolve(
  __dirname,
  '..',
  '..',
  'backend',
  'src',
  'templates',
  '_shared',
  'ai-website-editor-bridge.js'
);

// Load order matters: each file depends on functions declared by earlier files.
const SOURCE_FILES = [
  'domHelpers.js',
  'overlayManager.js',
  'selectorState.js',
  'eventHandlers.js',
  'dyad-component-selector-client.js',
  'dyad-visual-editor-client.js',
];

const parts = [
  '/* Auto-generated by frontend/scripts/build-editor-bridge.js */',
  '/* Do not edit directly; edit files in frontend/select-edit-feature/ and re-run the build script. */',
  '',
];

for (const file of SOURCE_FILES) {
  const filePath = path.join(SOURCE_DIR, file);
  if (!fs.existsSync(filePath)) {
    console.error(`Missing source file: ${filePath}`);
    process.exit(1);
  }
  parts.push(`/* ---------- ${file} ---------- */`);
  parts.push(fs.readFileSync(filePath, 'utf-8'));
  parts.push('');
}

fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
fs.writeFileSync(OUTPUT_FILE, parts.join('\n'), 'utf-8');

console.log(`Wrote ${OUTPUT_FILE} (${fs.statSync(OUTPUT_FILE).size} bytes)`);
